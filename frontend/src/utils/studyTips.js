// Функция для получения случайного совета
export const getRandomStudyTip = () => {
  const randomIndex = Math.floor(Math.random() * studyTips.length);
  return studyTips[randomIndex];
};

// Функция для получения совета дня (меняется каждый день)
export const getStudyTipOfTheDay = () => {
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / 1000 / 60 / 60 / 24);
  const tipIndex = dayOfYear % studyTips.length;
  return studyTips[tipIndex];
};